package ir.porteghali.java;

public class E27_ArithmeticOperator {
    public void Start (){
        System.out.println("E27 Arithmetic Operator");
        /*
         * +
         * -
         * *
         * /
         * %
         * ++
         * --
         */
        int a = 44 ;
        int b = 12 ;
        int c = a+b ;
        System.out.println(c);
        System.out.println(c++);
        System.out.println(++c);
    }
}
